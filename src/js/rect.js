/**
 * @author rrcat920
 * @description 表示一个矩形刚体
 */
class Rect {
  /**
   * @param {number} x x坐标
   * @param {number} y y坐标
   * @param {number} width 宽度
   * @param {number} height 高度
   */
  constructor(x, y, width, height) {
    this.x = x;
    this.y = y;
    this.width = width;
    this.height = height;
  }
}
